void main(){
  const a = 40;
  const b = 20;

  const add = a + b;
  const sub = a-b;
  const mul = a*b;
  const div = a/b;
  const mod = a % b;
  print("Addition : " + "40+20 = " + (add).toString());
  print("Addition : " + "40-20 = " + (sub).toString());
  print("Multiplication : " + "40*20 = " + (mul).toString());
  print("Division : " + "40/20 = " + (div).toString());
  print("Modulus : " + "40%20 = " + (mod).toString());


}